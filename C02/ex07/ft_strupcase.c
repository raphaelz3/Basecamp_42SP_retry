char	*ft_strupcase(char *str)
{
	char	*str2;

	str2 = str;
	while (*str != '\0')
	{
		if (*str >= 97 && *str <= 122)
		{
			*str = *str - 32;
		}
		str++;
	}
	str = str2;
	return (str);
}
