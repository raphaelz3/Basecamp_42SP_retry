char	*ft_strlowcase(char *str)
{
	char	*str2;

	str2 = str;
	while (*str != '\0')
	{
		if (*str >= 65 && *str <= 90)
		{
			*str = *str + 32;
		}
		str++;
	}
	str = str2;
	return (str);
}
